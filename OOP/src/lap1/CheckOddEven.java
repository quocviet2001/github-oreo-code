package lap1;

public class CheckOddEven {
    public static void main(String []args) {
        int number = 20;
        System.out.println("The number is " + number);
        if (number % 2 == 0) {
            System.out.println("EvenNumber");
        }
        else {
            System.out.println("OddNumber");
        }
        System.out.println("Bye!");
    }
}
